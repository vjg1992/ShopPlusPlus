const insertCatProducts = async () => {
    try {
        for (let productData of catProductsData) {
            const existingProduct = await CatProduct.findOne({ SKU: productData.SKU });
            if (!existingProduct) {
                productData.ProductID = await generateProductID(productData.Category);
                const catProduct = new CatProduct(productData);
                await catProduct.save();
            } else {
                console.log(`Product with SKU ${productData.SKU} already exists`);
            }
        }
        console.log('Cat products inserted');
    } catch (error) {
        console.error('Error inserting cat products:', error);
    }
};

module.exports = insertCatProducts;
